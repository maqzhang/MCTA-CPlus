#include "HeapSort.h"

/*
* 调正堆函数
* R：待排序数组；
* v：结点编号, 以 v 为根的二叉树, R[v] ≥ R[2v], R[v] ≥ R[2v + 1],
* 且其左子树和右子树都是大顶堆；
* n：堆结构的规模，即堆中的元素数
*/
// 参考;https://www.cnblogs.com/chengxiao/p/6129630.html
void Heapify(int R[MAXITEM], int v, int n) {
	int i, j;
	i = v;           // i 指向当前要调整的父节点位置
	j = 2 * i;       // j 指向 i 的左孩子节点
	R[0] = R[i];     // 将要调整的节点值暂存到 R[0] 中
	
	while (j <= n) { // 当左孩子存在时继续循环
		// 找到左右孩子中较大的那个
		if (j < n && R[j] < R[j + 1]) {
			j++;     // j 指向右孩子（较大的孩子）
		}
		
		// 如果父节点值小于较大的孩子节点值，需要调整
		//(1)
		if (R[0] < R[j]) {
			R[i] = R[j];    // 将较大的孩子节点上移
			i = j;          // 更新父节点位置
			j = 2 * i;      // 更新左孩子位置，继续向下调整
		}
		else {
			j = n + 1;      // 结束循环，堆性质已满足
		}
	}
	R[i] = R[0];        // 将暂存的值放到最终位置
}

/* 堆排序主函数，R 为待排序数组，n 为数组大小 */
void HeapSort(int R[MAXITEM], int n) {
	int i;
	
	// 第一阶段：建立初始大顶堆: 将给定无序序列构造成一个大顶堆（一般升序采用大顶堆，降序采用小顶堆)。
	// 从最后一个非叶子节点开始，向上逐个调整每个子树
	for (i = n / 2; i >= 1; i--) {
		//(2)
		Heapify(R, i, n);  // 调整以 i 为根的子树为大顶堆
	}
	
	// 第二阶段：排序过程
	// 重复取出堆顶最大元素，放到已排序区域的末尾
	//(3)
	for (i = n; i > 1; i--) {
		// 交换堆顶元素和当前堆的最后一个元素
		R[0] = R[i];        // 暂存最后一个元素
		R[i] = R[1];        // 将堆顶最大元素放到有序区域的开头
		//(4)
		R[1] = R[0];        // 将原最后一个元素放到堆顶
		
		// 重新调整剩余元素为大顶堆（堆大小减1）
		Heapify(R, 1, i - 1);
	}
}